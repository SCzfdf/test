package com.wfly.model;

/**
 * 培训学校
 * @author Administrator
 *
 */
public class School {
	private int id;
	private String school_name;
	private String school_type;
	private String school_address;
	private String school_create_time;
	private String school_space;
	private String school_course;
	private String tellphone;
	private String content;
	private String logo_url;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSchool_name() {
		return school_name;
	}
	public void setSchool_name(String school_name) {
		this.school_name = school_name;
	}
	public String getSchool_type() {
		return school_type;
	}
	public void setSchool_type(String school_type) {
		this.school_type = school_type;
	}
	public String getSchool_address() {
		return school_address;
	}
	public void setSchool_address(String school_address) {
		this.school_address = school_address;
	}
	public String getSchool_create_time() {
		return school_create_time;
	}
	public void setSchool_create_time(String school_create_time) {
		this.school_create_time = school_create_time;
	}
	public String getSchool_space() {
		return school_space;
	}
	public void setSchool_space(String school_space) {
		this.school_space = school_space;
	}
	public String getSchool_course() {
		return school_course;
	}
	public void setSchool_course(String school_course) {
		this.school_course = school_course;
	}
	public String getTellphone() {
		return tellphone;
	}
	public void setTellphone(String tellphone) {
		this.tellphone = tellphone;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getLogo_url() {
		return logo_url;
	}
	public void setLogo_url(String logo_url) {
		this.logo_url = logo_url;
	}
	
	
}
